package datastructures.utils;

/**
 * Utility class representing a pair of integers, (x, y). There are no restrictions for x or y.
 */
public class IntPair {
    // intentionally making these variables public, as there is no added value in providing getters/setters
    public int x;
    public int y;

    public IntPair(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
